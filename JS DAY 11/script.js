// =======================
// Operators
// =======================
// +, -, *, /
const fname = 'Amarjeet';
const lname = 'Ghosh';

console.log(fname + " " + lname); // Amarjeet Ghosh

// Template literals
console.log(`${fname} ${lname}`); // Amarjeet Ghosh

